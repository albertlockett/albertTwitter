create table TWEETS (
    TWEET_ID Number(20, 0),
    CONTENT VARCHAR2(141),
    TIME timestamp,
    USER_ID number(20, 0)
  );
  
create table USERS (
    USER_ID Number(20, 0),
    user_name varchar2(100),
    JOINED timestamp
  );
  
    
CREATE SEQUENCE SEQ_TWEET_ID INCREMENT BY 1 
